    @Action
    public void setRatio(){
        try{
            //Uzima se vrijednost omjera
            GlobalVariables.RATIO=Integer.parseInt(String.valueOf(jComboBox1.getSelectedItem()));
            //u glavnom prozoru se postavlja vrijednost omjera
            PioneerServerApp.Instance.getLabel2().setText("1 : "+GlobalVariables.RATIO);
            //zatvara se forma
            closeAboutBox();
        }catch(NumberFormatException ex){
            Log.getInstance().write("ERROR SetRatio 1: "+ex);
            this.jComboBox1.setSelectedIndex(0);
            JOptionPane.showMessageDialog(this, "Wrong ratio!","Error", JOptionPane.ERROR_MESSAGE);
        }
    }
   //Pritisak na dugme apply 
   @Action
    public void applyRatio(){
        try{
            //Uzima se vrijednost omjera
            GlobalVariables.RATIO=Integer.parseInt(String.valueOf(jComboBox1.getSelectedItem()));
            PioneerServerApp.Instance.getLabel2().setText("1 : "+GlobalVariables.RATIO);
            //Onemogucuje se tipka apply
            this.jButton3.setEnabled(false);
        }catch(NumberFormatException ex){
            Log.getInstance().write("ERROR SetRatio 2: "+ex);
            this.jComboBox1.setSelectedIndex(0);
            JOptionPane.showMessageDialog(this, "Wrong ratio!","Error", JOptionPane.ERROR_MESSAGE);
        }
    }
   //Promjena vrijednosti
   @Action
   public void changeRatio(){
       //Omogucuje se tipka Apply
       this.jButton3.setEnabled(true);
   }

